def decode_key_and_iv(inp):
    inp_len = len(inp)
    key = [0]*80
    iv = [0]*48

    # ####################
    for i in range(24):
        key[i] = inp[((i * 4) + 16) % inp_len]

    for i in range(8):
        iv[i] = inp[((i * 8) + 24) % inp_len]
    
    # ####################
    for i in range(24):
        key[i] ^= (((23 - i) ** 2) + 3) & 0xff

    for i in range(8):
        iv[i] ^= (((7 - i) ** 2) + 3) & 0xff
    
    # ####################
    for i in range(8):
        iv[i + 9] = inp[(i * 5 + 28) % inp_len]

    for i in range(8):
        iv[i + 18] = inp[(i * 7 + 15) % inp_len]
    
    # ####################
    for item in KEY_OPS:
        base_off = item[0]
        off = item[1]
        add_off = item[2]
        is_add = item[3]
        xor = item[4]

        a = inp[(off + add_off) % inp_len]
        b = inp[off] # was this a mistake? in any case, it lets us make KEY_OPS much shorter
        if is_add:
            key[base_off + off] = ((a + b) & 0xff) ^ xor
        else:
            key[base_off + off] = ((a - b) & 0xff) ^ xor

    # ####################
    for item in IV_OPS:
        base_off = item[0]
        off = item[1]
        add_off = item[2]
        is_add = item[3]
        xor = item[4]

        a = inp[(off + add_off) % inp_len]
        b = iv[off + base_off]
        if is_add:
            iv[base_off + off] = ((a + b) & 0xff) ^ xor
        else:
            iv[base_off + off] = ((a - b) & 0xff) ^ xor
    
    # ####################
    for i in range(24):
        key[i + 25] ^= (((23 - i) ** 2) + 3) & 0xff

    for i in range(8):
        iv[i + 9] ^= (((7 - i) ** 2) + 3) & 0xff
    
    # ####################
    for i in range(24):
        key[i + 50] ^= (((23 - i) ** 2) + 3) & 0xff

    for i in range(8):
        iv[i + 18] ^= (((7 - i) ** 2) + 3) & 0xff
    
    return (key, iv)